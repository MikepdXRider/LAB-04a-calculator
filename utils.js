
export const add = (firstInput, secondInput) =>{
    return firstInput + secondInput;
  };
  
  //SUBTRACT
  export const subtract = (firstInput, secondInput) =>{
    return firstInput - secondInput;
  };
  
  //MULTIPLY
  export const multiply = (firstInput, secondInput) =>{
    return firstInput * secondInput;
  };
  
  //DIVIDE
  export const divide = (firstInput, secondInput) =>{
    return firstInput / secondInput;
  };

